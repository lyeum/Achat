# 긴 키워드를 먼저 체크하기 위해 길이 내림차순 정렬
_SORTED_KEYWORDS: list[str] = sorted(_NARRATIONS.keys(), key=len, reverse=True)


def find_trigger(text: str) -> tuple[str, str] | None:
    """text에서 첫 번째로 매칭되는 (keyword, narration) 쌍을 반환한다.

    매칭 없으면 None.
    """
    for kw in _SORTED_KEYWORDS:
        if kw in text:
            return kw, random.choice(_NARRATIONS[kw])
    return None
